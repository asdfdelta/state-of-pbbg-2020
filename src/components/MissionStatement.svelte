<h2>We should come together.</h2>
<br />
<p>Since the very beginning of PBBGs, making friends and playing great games have been at the core of our experience.  Although our 
    community has gone on to build great games over the last 20 years, we have slowly drifted apart in the process.  There are so 
    many new PBBGs out there, with so many players that don't know about other games and communities of fellow players and future friends.
</p>

<p>The State of PBBG survey aims to make everyone's voice heard equally -- so that we can bring players and developers together to create 
    a better gaming world.
</p>

<p>Click the button below to take the survey and share this to help reach as many people as possible!</p>

